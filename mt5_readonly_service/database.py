def init_db() -> None:
    """
    Create all tables that do not yet exist.
    Idempotent: safe to call on every service startup.
    Existing data is never dropped or altered.
    """
    Base.metadata.create_all(bind=engine)


def get_db() -> Generator[Session, None, None]:
    """
    FastAPI Dependency -- yields a SQLAlchemy session for one request.

    Usage in an endpoint:
        def my_route(db: Session = Depends(get_db)):
            ...

    The session is always closed after the request, even on exceptions.
    """
    db = SessionLocal()
    try:
        yield db
    finally:
        db.close()
